package com.goit.oop.inheritance.ex1;

/**
 * Абстрактний клас AbstractClass.
 * Цей клас служить базою для інших класів, які наслідуються від нього.
 * Він містить абстрактний метод print(), який повинен бути реалізований в класах-нащадках.
 */
public abstract class AbstractClass {

    /**
     * Абстрактний метод print().
     * Цей метод повинен бути реалізований в класах, які наслідуються від цього класу.
     */
    protected abstract void print();
}