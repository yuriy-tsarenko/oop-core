package com.goit.oop.inheritance.ex1;

/**
 * Інтерфейс Interface.
 * Цей інтерфейс визначає метод print(), який повинен бути реалізований в класах, що його імплементують.
 */
public interface Interface {

    /**
     * Метод print().
     * Цей метод повинен бути реалізований в класах, що імплементують цей інтерфейс.
     */
    void print();
}