package com.goit.oop.inheritance.ex2;

/**
 * Клас DerivedClass є нащадком класу BaseClass.
 * Він містить публічні поля field4 та field5.
 */
public class DerivedClass extends BaseClass {
    // Публічне поле field4 класу DerivedClass
    public int field4;

    // Публічне поле field5 класу DerivedClass
    public int field5;
}