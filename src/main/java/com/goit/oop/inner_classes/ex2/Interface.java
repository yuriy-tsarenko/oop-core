package com.goit.oop.inner_classes.ex2;

/**
 * Анонімні класи.
 */
interface Interface {
    /**
     * Метод method(), який потрібно перевизначити в класах, що реалізують цей інтерфейс.
     */
    void method();

    /**
     * Метод getValue(), який потрібно перевизначити в класах, що реалізують цей інтерфейс.
     * Цей метод повинен повертати значення.
     */
    int getValue();
}